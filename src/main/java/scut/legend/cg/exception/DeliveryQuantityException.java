package scut.legend.cg.exception;

public class DeliveryQuantityException extends RuntimeException{

	public DeliveryQuantityException() {
		super();
	}

	public DeliveryQuantityException(String message, Throwable cause) {
		super(message, cause);
	}

	public DeliveryQuantityException(String message) {
		super(message);
	}

	public DeliveryQuantityException(Throwable cause) {
		super(cause);
	}
	
}
